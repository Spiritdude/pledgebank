 <div style="font-size:1.126em;padding-bottom:0.25em;">
   <p style="font-weight:bold;">
     'Adopt-a-street' encourages local volunteers to improve and care for their area by carrying out
     extra work which will make their neighbourhoods look more attractive, including planting, 
     gardening and litter sweeps.
  </p>
  <p>
     When residents sign up to adopt their street they will receive training from council officers 
     and be provided with appropriate equipment such as litter pickers, shovels and rubbish sacks. 
     Prior to the winter months, 'adopted' streets will also receive a delivery of grit and equipment 
     to remove snow and spread grit on their pavements. 
   </p>
   <ul>
     <li><a href="http://www.barnet.gov.uk/press_releases.htm?id=2401">Read more about the pilot scheme.</a></li>
    </ul>
